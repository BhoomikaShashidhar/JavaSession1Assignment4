
public class ExpressionValues {
	public static void main (String args[]){
		int x=5,y=10,a,b,c,d;
		a= x + y * 2;
		b= x - y + 2;
		c= (x+y) * 2;
		d= y % x;
	System.out.println("value of x+y*2=  "+a);
	System.out.println("value of x-y+2=  "+b);
	System.out.println("value of (x+y)*2=  "+c);
	System.out.println("value of y%x=  "+d);
	}
}
